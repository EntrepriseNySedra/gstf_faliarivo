Conformément à cet appel et en tenant compte de la grande diversité des séminaristes formés au Séminaire, on essaie de donner une place privilégiée à la vie communautaire et au respect que cela exige. Chaque année, le thème proposé tourne autour de l’importance de cette vie commune. Pour ce faire, des activités sont organisées ; elles peuvent être journalières ou hebdomadaires. Tous les séminaristes sont conviés à y participer et les répartitions se font généralement par groupe d’activités (au nombre de cinq). Chaque groupe d’activité est dirigé par un « chef » qui anime les membres à la réalisation de la tâche qui incombe.<br><br>
Au Séminaire, les organisations sportives servent aussi à tisser ce lien communautaire. Il y a à notre disposition des terrains de foot, de basket et de volley-ball. Ceux qui ne peuvent pas pratiquer aux jeux collectifs pour une raison particulière peuvent aussi pratiquer à d’autres jeux (ping-pong, pétanque…). Les jeux se font généralement après le repas de midi jusqu’à 14 heures trente. Des matches entre les séminaristes sont organisés chaque semestre (par promotion ou par groupe).

